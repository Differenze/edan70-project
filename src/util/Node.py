	# creates a new node from the string given in a dot file
	@staticmethod
	def new_from_string(dot_string):
		dot_string = dot_string.rstrip('\n;')

		parse = {
			'label'			: r'label="([^\]"]*)"',
			'debug'			: r'debug="([^\]"]*)"',
			'shape'			: r'shape=([^\s]*)\s',
			'style'			: r'style="([^\]"]*)"',
			'color'			: r'color="([^\]"]*)"',
			'length'		: r'\[l=([^\]]*)\]',
			'delay'			: r'\[d=([^\]]*)\]',
			'width'			: r'\[w=([^\]]*)\]',
			'operator_delay': r'\[od=([^\]]*)\]',
			'type_string'	: r'^(.*?)[_|\[]',
			'ID'			: r'^(.*?_?\d*)[\[|\s]',
		}
		info = {}

		for tag, regex in parse.iteritems():
			match = re.search(regex, dot_string)
			# print(match)
			if(match):
				info[tag] = match.group(1)
				# print(tag+': '+info[tag])
		# print(info)
		# node = Node(info['type_str'], info['ID'], info['label'], info)
		# print(dot_string, info)
		node = Node(**info)
		return node

